# Program to calculate sum, difference, perimeter, and area, and evaluate the given expression

# Taking two numbers as input from the user
num1 = int(input("Enter first number: "))  # User enters the first number
num2 = int(input("Enter second number: "))  # User enters the second number

# Calculating sum and difference
sum_result = num1 + num2  # Adding the two numbers
difference_result = num1 - num2  # Subtracting second number from the first

# Displaying sum and difference
print("Sum of the two numbers:", sum_result)  
print("Difference of the two numbers:", difference_result)  