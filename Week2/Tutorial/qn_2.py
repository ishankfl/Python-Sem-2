
# Taking length and breadth as input to calculate perimeter and area
length = float(input("Enter length of the rectangle: "))  # User enters the length
breadth = float(input("Enter breadth of the rectangle: "))  # User enters the breadth

# Calculating perimeter and area of a rectangle
perimeter = 2 * (length + breadth)  # Formula for perimeter of a rectangle
area = length * breadth  # Formula for area of a rectangle

# Displaying perimeter and area
print("Perimeter of the rectangle:", perimeter)  
print("Area of the rectangle:", area)  